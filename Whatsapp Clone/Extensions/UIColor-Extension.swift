import Foundation
import UIKit

extension UIColor {
    
    class var lightGray: UIColor {
        return UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 1.0)
    }
    
    class var lightBlue: UIColor {
        return UIColor(red: 190/255, green: 213/255, blue: 250/255, alpha: 1.0)
    }
    
    class var darkBlue: UIColor {
        return UIColor(red: 4/255, green: 56/255, blue: 255/255, alpha: 1.0)
    }
    
}
